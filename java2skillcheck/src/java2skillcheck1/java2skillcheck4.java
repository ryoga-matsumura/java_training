package java2skillcheck1;

/**
 * 2.演算子　実力確認問題
 */
public class java2skillcheck4 {
	/**
	 * int 型の変数 x に数値 7 を代入する。
	 * 変数 x の値を 3 倍にする。
	 * 変数 x の値を表示する。
	 * 変数 x の値を半分(1/2)にする。
	 * 変数 x の値を表示する。
	 * @param args 整数
	 */
	public static void main(String[] args) {
		int x = 7;
		x *= 3;
		System.out.println(x);
		x /= 2;
		System.out.println(x);
	}
}
